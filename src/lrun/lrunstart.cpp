bool LRUN_LRunStart::StartRunner(ASFC_Screen* poScreen, string sGame, int iMap, int iX, int iY)
{	//Define game objectss
		LRUN_LoreAndLutes oLore(poScreen);
	//Setup the help system
		LL_Help oHelp;
		oHelp.Setup(&oLore.Painter(), poScreen);
		oLore.SetHelp(&oHelp);
	//Load a game
		oLore.Game().SetGameFile(sGame);
		oLore.LoadGame("./games/" + sGame + "/" + sGame + ".game", "");
	//Set the player to the right map if neccessary
		if(iMap != -1)
		{	//Load the map
				oLore.Game().LoadMap(iMap);
			//Load classes
				oLore.Game().LoadClasses("", poScreen);
			//Reposition the player
				oLore.Game().Player().Place(iX, iY);
			//Init for the newly loaded map
				oLore.InitForNewMap();
		}
  	//Run game
		while(oLore.RunGame())
  		{	
		}
		
		return(oLore.ReturnToStarter());
}
